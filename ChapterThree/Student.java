public class Student
{
   private int sNumber;
   private int creditHrs;
   private double gpa;
   
   public void setSNumber(int s)
   {
      sNumber = s;
   }
   
   public int getSNumber()
   {
      return sNumber;
   }
   
   public void setCreditHrs(int c)
   {
      creditHrs = c;
   }
   
   public int getCreditHrs()
   {
      return creditHrs;
   }
   
   public void setGpa(double c)
   {
      gpa = c;
   }
   
   public double getGpa()
   {
      return gpa;
      
   }
   

}